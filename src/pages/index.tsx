function Feed() {
  const { data, isLoading, isError } = api.mDB.getTrending.useQuery();

  if (isError) return <div>Something went wrong</div>;
  if (isLoading) return <div>Loading...</div>;

  return (
    <div>
      <MediaRow
        title={"Trending Shows"}
        media={data.trendingShows}
        bgColor="bg-zinc-800"
      />
      <MediaRow
        title={"Popular Shows"}
        media={data.popularShows}
        bgColor="bg-zinc-700"
      />
      <MediaRow
        title={"Popular Movies"}
        media={data.popularMovies}
        bgColor="bg-zinc-800"
      />
    </div>
  );
}

function MediaRow({
  title,
  media,
  bgColor,
}: {
  title: string;
  media: MovieAPIResult[] | ShowAPIResult[];
  bgColor?: string;
}) {
  const size = useWindowSize();
  const slidesPerView =
    size.width && size.width < 440
      ? 2
      : size.width && size.width < 510
      ? 2.5
      : size.width && size.width < 595
      ? 3
      : size.width && size.width < 680
      ? 3.5
      : size.width && size.width < 760
      ? 4
      : size.width && size.width < 870
      ? 4.5
      : 5;

  return (
    <div className={`p-4 ${bgColor}`}>
      <h2 className="py-2 text-xl font-bold tracking-wider">{title}</h2>
      <div>
        <Swiper
          className="w-full"
          modules={[Navigation, Pagination, Scrollbar, A11y]}
          slidesPerView={slidesPerView}
          spaceBetween={10}
          loop={true}
          navigation={true}
          centeredSlidesBounds={true}
        >
          {media.map((show) => (
            <SwiperSlide key={show.id}>
              <MediaCard media={show} />
            </SwiperSlide>
          ))}
        </Swiper>
      </div>
    </div>
  );
}

